void vga_put_pixel(int x, int y, unsigned char color) {
    if(x >= 0 && x < SCREEN_WIDTH && y >= 0 && y < SCREEN_HEIGHT) {
        vga_buffer[y * SCREEN_WIDTH + x] = color;
    }
}

void vga_draw_rect(int x, int y, int w, int h, unsigned char color) {
    for(int j = 0; j < h; j++) {
        for(int i = 0; i < w; i++) {
            vga_put_pixel(x + i, y + j, color);
        }
    }
}

void vga_clear_gfx() {
    for(int i = 0; i < SCREEN_WIDTH * SCREEN_HEIGHT; i++) {
        vga_buffer[i] = VGA_COLOR_BLACK;
    }
    vga_cursor_x = 5;
    vga_cursor_y = 5;
}

void vga_scroll() {
    for(int y = 0; y < SCREEN_HEIGHT - 10; y++) {
        for(int x = 0; x < SCREEN_WIDTH; x++) {
            vga_buffer[y * SCREEN_WIDTH + x] = vga_buffer[(y + 10) * SCREEN_WIDTH + x];
        }
    }
    for(int y = SCREEN_HEIGHT - 10; y < SCREEN_HEIGHT; y++) {
        for(int x = 0; x < SCREEN_WIDTH; x++) {
            vga_buffer[y * SCREEN_WIDTH + x] = VGA_COLOR_BLACK;
        }
    }
    vga_cursor_y -= 10;
}

void vga_draw_char(char c, int x, int y, unsigned char color) {
    if ((unsigned char)c > 127) return;
    unsigned volatile char* glyph = font8x8[(int)c];
    for(int row = 0; row < 8; row++) {
        unsigned char bits = glyph[row];
        for(int col = 0; col < 8; col++) {
            if(bits & (0x80 >> col)) {
                vga_put_pixel(x + col, y + row, color);
            }
        }
    }
}

void vga_putchar(char c, unsigned char color) {
    if (c == '\b') {
        if (vga_cursor_x > 45) {
            vga_cursor_x -= 8;
            vga_draw_rect(vga_cursor_x, vga_cursor_y, 8, 8, VGA_COLOR_BLACK);
        }
        return;
    }
    if (c == '\n') {
        vga_cursor_x = 5;
        vga_cursor_y += 10;
    } else {
        vga_draw_char(c, vga_cursor_x, vga_cursor_y, color);
        vga_cursor_x += 8;
    }

    if (vga_cursor_x > SCREEN_WIDTH - 8) {
        vga_cursor_x = 5;
        vga_cursor_y += 10;
    }

    if (vga_cursor_y > SCREEN_HEIGHT - 10) {
        vga_scroll();
    }
}

void vga_print_string(const volatile char* str, unsigned char color) {
    while(*str) {
        vga_putchar(*str, color);
        str++;
    }
}

// NEW: String helpers
int string_compare(const volatile char* s1, const volatile char* s2) {
    while (*s1 && (*s1 == *s2)) {
        s1++; s2++;
    }
    return *(unsigned volatile char*)s1 - *(unsigned volatile char*)s2;
}

int str_startswith(volatile char* str, volatile char* prefix) {
    while(*prefix) {
        if(*str++!= *prefix++) return 0;
    }
    return 1;
}

uint8_t get_vga_color(volatile char* name) {
    if(string_compare(name, "red") == 0) return VGA_COLOR_RED;
    if(string_compare(name, "green") == 0) return VGA_COLOR_GREEN;
    if(string_compare(name, "blue") == 0) return VGA_COLOR_BLUE;
    if(string_compare(name, "yellow") == 0) return VGA_COLOR_YELLOW;
    if(string_compare(name, "cyan") == 0) return VGA_COLOR_LIGHT_CYAN;
    if(string_compare(name, "white") == 0) return VGA_COLOR_WHITE;
    if(string_compare(name, "magenta") == 0) return VGA_COLOR_MAGENTA;
    if(string_compare(name, "black") == 0) return VGA_COLOR_BLACK;
    return VGA_COLOR_WHITE;
}

// NEW: Run single command
void run_single_cmd(volatile char* cmd) {
    while(*cmd == ' ') cmd++; // Trim start space

    int len = 0;
    while(cmd[len]!= '\0') len++;
    while(len > 0 && cmd[len-1] == ' ') cmd[--len] = '\0'; // Trim end space

    if (len == 0) return;

    if (string_compare(cmd, "help") == 0) {
        vga_print_string("Commands:\n", VGA_COLOR_YELLOW);
        vga_print_string(" help - Show this help\n", current_text_color);
        vga_print_string(" clear - Clear screen\n", current_text_color);
        vga_print_string(" about - About OS\n", current_text_color);
        vga_print_string(" ls - List files\n", current_text_color);
        vga_print_string(" ps - List processes\n", current_text_color);
        vga_print_string(" whoami - Current user\n", current_text_color);
        vga_print_string(" echo - Print text\n", current_text_color);
        vga_print_string(" color - Change text color\n", current_text_color);
        vga_print_string(" reboot - Restart OS\n", current_text_color);
    }
    else if (string_compare(cmd, "clear") == 0) {
        vga_clear_gfx();
    }
    else if (string_compare(cmd, "about") == 0) {
        vga_print_string("SOUMYAJIT OS v2.1\n", VGA_COLOR_WHITE);
        vga_print_string("Made with love <3\n", VGA_COLOR_MAGENTA);
        vga_print_string("Mode 13h Graphics | 100MB RAM\n", VGA_COLOR_LIGHT_CYAN);
        vga_print_string("Shell v1.0 Ready\n", VGA_COLOR_GREEN);
    }
    else if (string_compare(cmd, "ls") == 0) {
        vga_print_string("kernel.bin 64KB\n", current_text_color);
        vga_print_string("vga.sys 12KB\n", current_text_color);
        vga_print_string("boot.img 1KB\n", current_text_color);
        vga_print_string("readme.txt 2KB\n", VGA_COLOR_LIGHT_CYAN);
    }
    else if (string_compare(cmd, "ps") == 0) {
        vga_print_string("PID NAME\n", VGA_COLOR_YELLOW);
        vga_print_string(" 1 kernel\n", current_text_color);
        vga_print_string(" 2 keyboard_driver\n", current_text_color);
        vga_print_string(" 3 vga_driver\n", current_text_color);
        vga_print_string(" 4 shell\n", current_text_color);
    }
    else if (string_compare(cmd, "whoami") == 0) {
        vga_print_string("root@soumyajit-os\n", VGA_COLOR_LIGHT_CYAN);
    }
    else if (str_startswith(cmd, "echo ")) {
        vga_print_string(cmd + 5, current_text_color);
        vga_putchar('\n', current_text_color);
    }
    else if (str_startswith(cmd, "color ")) {
        volatile char* color_name = cmd + 6;
        current_text_color = get_vga_color(color_name);
        vga_print_string("Text color changed to ", VGA_COLOR_WHITE);
        vga_print_string(color_name, current_text_color);
        vga_putchar('\n', VGA_COLOR_WHITE);
    }
    else if (string_compare(cmd, "reboot") == 0) {
        vga_print_string("Rebooting...\n", VGA_COLOR_RED);
        outb(0x64, 0xFE);
    }
    else {
        vga_print_string("Unknown: ", VGA_COLOR_RED);
        vga_print_string(cmd, VGA_COLOR_RED);
        vga_putchar('\n', VGA_COLOR_RED);
    }
}

// NEW: Process command with semicolon support
void process_command() {
    char temp[256];
    int i = 0, j = 0;

    while(cmd_buffer[i]!= '\0') {
        if(cmd_buffer[i] == ';') {
            temp[j] = '\0';
            run_single_cmd(temp);
            j = 0;
            i++;
            while(cmd_buffer[i] == ' ') i++;
        } else {
            temp[j++] = cmd_buffer[i++];
        }
    }
    temp[j] = '\0';
    if(j > 0) run_single_cmd(temp);

    cmd_buffer[0] = '\0';
    cmd_idx = 0;
}

// UPDATED: Keyboard handler
void vga_handle_keyboard_input(char c) {
    if (c == '\n') {
        cmd_buffer[cmd_idx] = '\0';
        vga_putchar('\n', VGA_COLOR_WHITE);
        process_command(); // NEW: semicolon support
        vga_print_string("SOC> ", VGA_COLOR_LIGHT_CYAN);

    } else if (c == '\b') {
        if (cmd_idx > 0) {
            cmd_idx--;
            vga_putchar('\b', VGA_COLOR_WHITE);
        }
    } else if (c >= ' ' && c <= '~') {
        if (cmd_idx < 255) {
            cmd_buffer[cmd_idx++] = c;
            vga_putchar(c, current_text_color); // NEW: use current color
        }
    }
}

